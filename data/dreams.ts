export function getDreamBySlug(slug: string): Dream | undefined {
  return dreams.find((d) => d.slug === slug);
}

export function getDreamsByLetter(letter: string): Dream[] {
  return dreams.filter((d) => d.letter === letter);
}

export function getRelatedDreams(slugs: string[]): Dream[] {
  return dreams.filter((d) => slugs.includes(d.slug));
}

export function searchDreams(query: string): Dream[] {
  const q = query.toLowerCase();
  return dreams.filter(
    (d) =>
      d.title.toLowerCase().includes(q) ||
      d.tags.some((t) => t.toLowerCase().includes(q))
  );
}

