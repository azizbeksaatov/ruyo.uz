export function getNameBySlug(slug: string): NameEntry | undefined {
  return names.find((n) => n.slug === slug);
}

export function getNamesByOrigin(origin: NameOrigin): NameEntry[] {
  return names.filter((n) => n.origin === origin);
}

export function searchNames(query: string): NameEntry[] {
  const q = query.toLowerCase();
  return names.filter((n) => n.name.toLowerCase().includes(q) || n.meaning.toLowerCase().includes(q));
}

