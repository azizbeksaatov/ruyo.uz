export async function POST(req: NextRequest) {
  const { birthDate, gender, locale } = await req.json();

  if (!birthDate) {
    return NextResponse.json({ error: 'Missing birth date' }, { status: 400 });
  }

  const date = new Date(birthDate);
  const day = date.getDate();
  const month = date.getMonth() + 1;
  const sign = getZodiacSign(day, month);
  const reading = STATIC_READINGS[sign.sign];

  const signName = locale === 'uz' ? sign.uz : sign.ru;

  const result: Record<string, unknown> = {
    sign: sign.sign,
    signRu: signName,
    signEmoji: sign.emoji,
    period: sign.dates,
    ...reading,
  };

  // Optionally enrich with AI if API key is available
  if (process.env.GEMINI_API_KEY) {
    try {
      const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
      const model = genAI.getGenerativeModel({ model: 'gemini-2.0-flash' });
      const lang = locale === 'uz' ? "o'zbek" : 'русском';
      const msg = await model.generateContent(
        `Составь персональный совет для ${sign.ru} (${gender === 'female' ? 'женщина' : 'мужчина'}, дата рождения ${birthDate}). 2-3 предложения на ${lang} языке. Только совет, без вступления.`
      );
      result.advice = msg.response.text();
    } catch {
      // keep static advice
    }
  }

  return NextResponse.json(result);
}
